"""Algebraic measurement toolkit for self-modeling experiments.

Measures:
1. Commutator norms: ||[W_i, W_j]||_F / (||W_i||_F * ||W_j||_F)
2. Generated algebra dimension: rank of product matrix via SVD
3. Block structure detection: spectral clustering on |W| adjacency
"""

import numpy as np
from itertools import product as iterproduct
import json
import time


def random_commutator_baseline(N):
    """Theoretical commutator norm for random NxN matrices: sqrt(2/N).

    The normalized commutator ||[A,B]||_F / (||A||_F * ||B||_F) scales as
    sqrt(2/N) for random Gaussian matrices, not sqrt(2). This is because
    ||A||_F * ||B||_F grows as N^2 * sigma^2 while ||[A,B]||_F grows as
    sqrt(2) * N^{3/2} * sigma^2.
    """
    return np.sqrt(2.0 / N)


def commutator_norm(W_i, W_j):
    """Normalized Frobenius norm of [W_i, W_j].

    Returns scalar. Random baseline is sqrt(2/N) where N is matrix size.
    Requires square matrices of same size.
    """
    assert W_i.shape == W_j.shape and W_i.shape[0] == W_i.shape[1]
    comm = W_i @ W_j - W_j @ W_i
    norm = np.linalg.norm(comm, 'fro')
    denom = np.linalg.norm(W_i, 'fro') * np.linalg.norm(W_j, 'fro')
    if denom < 1e-12:
        return 0.0
    return float(norm / denom)


def all_commutator_norms(matrices, names=None):
    """Compute commutator norms for all pairs of compatible square matrices.

    Returns dict mapping (name_i, name_j) -> norm.
    """
    if names is None:
        names = [f'W{i}' for i in range(len(matrices))]
    results = {}
    for i in range(len(matrices)):
        for j in range(i + 1, len(matrices)):
            Wi, Wj = matrices[i], matrices[j]
            if Wi.shape == Wj.shape and Wi.shape[0] == Wi.shape[1]:
                results[(names[i], names[j])] = commutator_norm(Wi, Wj)
    return results


def _generate_products(matrices, max_depth, max_memory_gb=2.0):
    """Generate all products of matrices up to given depth.

    Returns list of flattened product vectors (each length N^2).
    Includes identity at depth 0.
    """
    N = matrices[0].shape[0]
    k = len(matrices)

    # Estimate memory: total products * N^2 * 4 bytes
    total_products = 1  # identity
    for d in range(1, max_depth + 1):
        total_products += k ** d
    est_bytes = total_products * N * N * 4
    est_gb = est_bytes / 1e9
    if est_gb > max_memory_gb:
        raise MemoryError(
            f"Product matrix would use ~{est_gb:.1f}GB "
            f"(limit {max_memory_gb}GB). Reduce depth or matrix count."
        )

    # Identity
    products = [np.eye(N, dtype=np.float32)]

    # Build products iteratively by depth
    # prev_depth_products[i] is the matrix for index-tuple i
    prev_depth = [m.astype(np.float32) for m in matrices]
    products.extend(prev_depth)

    for d in range(2, max_depth + 1):
        new_depth = []
        for prev in prev_depth:
            for m in matrices:
                new_depth.append(prev @ m)
        products.extend(new_depth)
        prev_depth = new_depth

    return products


def algebra_dimension(matrices, max_depth=4, threshold='auto',
                      return_spectrum=True):
    """Compute dimension of algebra generated by given square matrices.

    Args:
        matrices: list of NxN numpy arrays
        max_depth: maximum product depth
        threshold: 'auto' for numpy default, or float for fixed threshold
        return_spectrum: if True, also return singular values

    Returns:
        dict with 'dimension', 'n_squared', 'num_products',
        and optionally 'singular_values'
    """
    for m in matrices:
        assert m.shape[0] == m.shape[1], f"Matrix not square: {m.shape}"
    N = matrices[0].shape[0]
    for m in matrices:
        assert m.shape == (N, N), f"Matrices must be same size, got {m.shape}"

    products = _generate_products(matrices, max_depth)
    num_products = len(products)

    # Vectorize: each product -> row of length N^2
    P = np.stack([p.flatten() for p in products])  # shape: (num_products, N^2)

    # SVD for rank
    sv = np.linalg.svd(P, compute_uv=False)

    if threshold == 'auto':
        # numpy default: eps * max(M, N) * sigma_max
        eps = np.finfo(np.float32).eps
        thr = eps * max(P.shape) * sv[0] if sv[0] > 0 else 0
    else:
        thr = threshold

    dim = int(np.sum(sv > thr))

    # dependency_ratio: 1.0 = all products independent (no constraints)
    # < 1.0 = some products are dependent (algebraic constraints detected)
    dep_ratio = dim / num_products if num_products > 0 else 1.0

    result = {
        'dimension': dim,
        'n_squared': N * N,
        'num_products': num_products,
        'dependency_ratio': dep_ratio,
        'max_depth': max_depth,
        'threshold_used': float(thr),
    }
    if return_spectrum:
        result['singular_values'] = sv.tolist()
    return result


def algebra_dimension_by_depth(matrices, max_depth=5):
    """Track algebra dimension as depth increases. Useful for convergence check."""
    dims = {}
    for d in range(1, max_depth + 1):
        try:
            r = algebra_dimension(matrices, max_depth=d, return_spectrum=False)
            dims[d] = r['dimension']
        except MemoryError:
            break
    return dims


def detect_blocks(matrices, max_clusters=8):
    """Detect block structure via spectral clustering on summed |W|.

    Only meaningful if algebra dimension < N^2.

    Returns dict with 'n_blocks', 'block_sizes', 'coupling_ratio',
    'eigenvalues' (of normalized Laplacian).
    """
    N = matrices[0].shape[0]

    # Sum absolute values as adjacency matrix
    A = np.zeros((N, N), dtype=np.float32)
    for m in matrices:
        A += np.abs(m)

    # Degree matrix and normalized Laplacian
    deg = A.sum(axis=1)
    deg_inv_sqrt = np.where(deg > 1e-12, 1.0 / np.sqrt(deg), 0.0)
    D_inv_sqrt = np.diag(deg_inv_sqrt)
    L = np.eye(N) - D_inv_sqrt @ A @ D_inv_sqrt

    # Eigendecompose
    eigvals, eigvecs = np.linalg.eigh(L)

    # Find spectral gap: largest jump in sorted eigenvalues (among first max_clusters)
    gaps = np.diff(eigvals[:max_clusters + 1])
    if len(gaps) == 0:
        return {'n_blocks': 1, 'block_sizes': [N], 'coupling_ratio': 0.0,
                'eigenvalues': eigvals[:max_clusters + 1].tolist()}

    best_gap_idx = np.argmax(gaps)
    n_blocks = best_gap_idx + 1

    if n_blocks < 2:
        return {'n_blocks': 1, 'block_sizes': [N], 'coupling_ratio': 0.0,
                'eigenvalues': eigvals[:max_clusters + 1].tolist(),
                'gap_sizes': gaps.tolist()}

    # Spectral clustering using eigenvectors below the gap
    features = eigvecs[:, :n_blocks]

    # Simple k-means (avoid sklearn dependency)
    labels = _kmeans(features, n_blocks)

    # Block sizes
    block_sizes = sorted([int(np.sum(labels == k)) for k in range(n_blocks)],
                         reverse=True)

    # Coupling ratio: inter-block / intra-block norms
    intra = 0.0
    inter = 0.0
    for m in matrices:
        for bi in range(n_blocks):
            for bj in range(n_blocks):
                mask_i = labels == bi
                mask_j = labels == bj
                block = m[np.ix_(mask_i, mask_j)]
                norm = np.linalg.norm(block, 'fro') ** 2
                if bi == bj:
                    intra += norm
                else:
                    inter += norm

    coupling_ratio = float(inter / intra) if intra > 1e-12 else float('inf')

    return {
        'n_blocks': n_blocks,
        'block_sizes': block_sizes,
        'coupling_ratio': coupling_ratio,
        'eigenvalues': eigvals[:max_clusters + 1].tolist(),
        'gap_sizes': gaps.tolist(),
        'labels': labels.tolist(),
    }


def _kmeans(X, k, max_iter=100, n_init=10):
    """Simple k-means. Returns labels array."""
    n, d = X.shape
    best_labels = None
    best_inertia = float('inf')

    for _ in range(n_init):
        # Random initialization
        idx = np.random.choice(n, k, replace=False)
        centers = X[idx].copy()

        for _ in range(max_iter):
            # Assign
            dists = np.sum((X[:, None, :] - centers[None, :, :]) ** 2, axis=2)
            labels = np.argmin(dists, axis=1)

            # Update
            new_centers = np.zeros_like(centers)
            for j in range(k):
                members = X[labels == j]
                if len(members) > 0:
                    new_centers[j] = members.mean(axis=0)
                else:
                    new_centers[j] = X[np.random.randint(n)]

            if np.allclose(centers, new_centers):
                break
            centers = new_centers

        inertia = sum(np.sum((X[labels == j] - centers[j]) ** 2)
                      for j in range(k))
        if inertia < best_inertia:
            best_inertia = inertia
            best_labels = labels.copy()

    return best_labels


def peirce_mixing_test(W):
    """Test whether off-diagonal coupling follows sqrt(lambda_i * lambda_j).

    Paper 5's sequential product has Peirce 1-space coupling proportional
    to the geometric mean of eigenvalues. If a weight matrix implements
    this structure, the ratio of actual off-diagonal coupling to predicted
    should be approximately constant across all eigenvalue pairs.

    Returns dict with 'mean_ratio', 'std_ratio', 'ratios' (per pair),
    and 'symmetry_ratio' (||W_sym|| / ||W||).
    """
    assert W.shape[0] == W.shape[1]
    N = W.shape[0]

    # Spectral decomposition
    eigvals, eigvecs = np.linalg.eigh((W + W.T) / 2)  # symmetrize first

    # Sort by descending |eigenvalue|
    idx = np.argsort(-np.abs(eigvals))
    eigvals = eigvals[idx]
    eigvecs = eigvecs[:, idx]

    # Use top k eigenvalues (skip near-zero ones)
    k = min(10, N)
    threshold = np.abs(eigvals[0]) * 0.01
    k = max(2, np.sum(np.abs(eigvals) > threshold))
    k = min(k, 10)

    ratios = []
    for i in range(k):
        for j in range(i + 1, k):
            li, lj = np.abs(eigvals[i]), np.abs(eigvals[j])
            if li < 1e-10 or lj < 1e-10:
                continue
            expected = np.sqrt(li * lj)

            # Peirce 1-space projection: component of W connecting
            # eigenspace i and eigenspace j
            vi = eigvecs[:, i:i+1]  # (N, 1)
            vj = eigvecs[:, j:j+1]  # (N, 1)
            # Off-diagonal coupling: vi^T W vj
            actual = np.abs((vi.T @ W @ vj).item())

            if expected > 1e-10:
                ratios.append(actual / expected)

    if len(ratios) == 0:
        return {'mean_ratio': 0.0, 'std_ratio': 0.0, 'n_pairs': 0,
                'symmetry_ratio': 0.5}

    # Symmetry ratio: how symmetric is W?
    W_sym = (W + W.T) / 2
    W_norm = np.linalg.norm(W, 'fro')
    sym_ratio = float(np.linalg.norm(W_sym, 'fro') / W_norm) if W_norm > 1e-12 else 0.5

    return {
        'mean_ratio': float(np.mean(ratios)),
        'std_ratio': float(np.std(ratios)),
        'n_pairs': len(ratios),
        'consistency': float(1.0 - np.std(ratios) / (np.mean(ratios) + 1e-10)),
        'symmetry_ratio': sym_ratio,
    }


def weight_symmetry(model):
    """Measure symmetric/antisymmetric decomposition of each weight matrix.

    Vanchurin: symmetric weights -> scalar fields, antisymmetric -> spinor.
    Tracks r_sym = ||W_sym||_F / ||W||_F for each layer.

    Returns dict mapping layer name -> r_sym.
    """
    import torch
    results = {}
    for name, param in model.named_parameters():
        if 'weight' in name and param.dim() == 2:
            w = param.detach().cpu().numpy()
            if w.shape[0] != w.shape[1]:
                continue
            w_sym = (w + w.T) / 2
            w_norm = np.linalg.norm(w, 'fro')
            if w_norm > 1e-12:
                results[name] = float(np.linalg.norm(w_sym, 'fro') / w_norm)
            else:
                results[name] = 0.5
    return results


def extract_square_weights(model, min_size=16):
    """Extract square weight matrices from a PyTorch model.

    Returns list of (name, numpy_array) for square weight matrices
    with size >= min_size.
    """
    import torch
    results = []
    for name, param in model.named_parameters():
        if 'weight' in name and param.dim() == 2:
            w = param.detach().cpu().numpy()
            if w.shape[0] == w.shape[1] and w.shape[0] >= min_size:
                results.append((name, w))
    return results


def extract_algebra_matrices(model, method='square'):
    """Extract matrices for algebra computation from a self-modeling MLP.

    method='square': only square weight matrices
    method='compose': also include composed rectangular matrices
    """
    import torch
    square = []
    names = []

    for name, param in model.named_parameters():
        if 'weight' in name and param.dim() == 2:
            w = param.detach().cpu().numpy()
            if w.shape[0] == w.shape[1]:
                square.append(w)
                names.append(name)

    if method == 'compose':
        # Look for self-prediction encode/decode pair
        sp_encode = sp_decode = None
        for name, param in model.named_parameters():
            if 'weight' in name and param.dim() == 2:
                w = param.detach().cpu().numpy()
                if 'sp_encode' in name or 'selfpred_encode' in name:
                    sp_encode = (name, w)
                elif 'sp_decode' in name or 'selfpred_decode' in name:
                    sp_decode = (name, w)

        if sp_encode is not None and sp_decode is not None:
            # Compose: decode @ encode gives NxN
            composed = sp_decode[1] @ sp_encode[1]
            if composed.shape[0] == composed.shape[1]:
                square.append(composed)
                names.append('sp_composed')

    return square, names


def weight_stats(model):
    """Compute distribution statistics for each weight matrix."""
    import torch
    stats = {}
    for name, param in model.named_parameters():
        if 'weight' in name and param.dim() == 2:
            w = param.detach().cpu().numpy().flatten()
            stats[name] = {
                'mean': float(np.mean(w)),
                'std': float(np.std(w)),
                'kurtosis': float(_kurtosis(w)),
                'min': float(np.min(w)),
                'max': float(np.max(w)),
            }
    return stats


def _kurtosis(x):
    """Excess kurtosis (normal = 0)."""
    m = np.mean(x)
    s = np.std(x)
    if s < 1e-12:
        return 0.0
    return float(np.mean(((x - m) / s) ** 4) - 3.0)


def measure_all(model, method='compose'):
    """Run all algebraic metrics on a model.

    Returns dict with all results, JSON-serializable.
    """
    t0 = time.time()

    matrices, names = extract_algebra_matrices(model, method=method)

    if len(matrices) == 0:
        return {'error': 'No square weight matrices found', 'elapsed': 0}

    results = {
        'matrix_names': names,
        'matrix_sizes': [m.shape[0] for m in matrices],
        'num_matrices': len(matrices),
    }

    N = matrices[0].shape[0]

    # Commutator norms
    cn = all_commutator_norms(matrices, names)
    results['commutator_norms'] = {f'{k[0]}_vs_{k[1]}': v for k, v in cn.items()}
    results['commutator_random_baseline'] = float(random_commutator_baseline(N))

    # Algebra dimension
    alg = algebra_dimension(matrices, max_depth=4)
    results['algebra_dimension'] = alg['dimension']
    results['n_squared'] = alg['n_squared']
    results['num_products'] = alg['num_products']
    results['dependency_ratio'] = alg['dependency_ratio']
    results['singular_values'] = alg['singular_values']

    # Block structure (only if dependencies detected)
    if alg['dependency_ratio'] < 0.99:
        blocks = detect_blocks(matrices)
        results['block_structure'] = blocks
    else:
        results['block_structure'] = None

    # Weight stats
    results['weight_stats'] = weight_stats(model)

    # Peirce mixing function test (NEW)
    peirce_results = {}
    for name, m in zip(names, matrices):
        peirce_results[name] = peirce_mixing_test(m)
    results['peirce_mixing'] = peirce_results
    # Aggregate: mean consistency across all matrices
    consistencies = [v['consistency'] for v in peirce_results.values()
                     if v['n_pairs'] > 0]
    results['peirce_consistency'] = float(np.mean(consistencies)) if consistencies else 0.0

    # Weight symmetry decomposition (NEW)
    results['weight_symmetry'] = weight_symmetry(model)

    results['elapsed_seconds'] = time.time() - t0
    return results


def save_results(results, path):
    """Save results dict as JSON."""
    with open(path, 'w') as f:
        json.dump(results, f, indent=2, default=str)


def print_summary(results):
    """Print human-readable summary of measurement results."""
    print(f"\n{'='*60}")
    print(f"ALGEBRAIC MEASUREMENT SUMMARY")
    print(f"{'='*60}")
    print(f"Matrices analyzed: {results.get('num_matrices', '?')} "
          f"({', '.join(results.get('matrix_names', []))})")
    print(f"Matrix size: {results.get('matrix_sizes', ['?'])[0]}x"
          f"{results.get('matrix_sizes', ['?'])[0]}")

    dep = results.get('dependency_ratio', 1.0)
    dim = results.get('algebra_dimension', '?')
    nprod = results.get('num_products', '?')
    print(f"\nAlgebra: dim={dim} / {nprod} products "
          f"(dependency ratio: {dep:.4f})")
    if dep >= 0.99:
        print(f"  -> All products independent (no algebraic constraints)")
    else:
        print(f"  -> {nprod - dim} dependent products "
              f"(algebraic constraints detected)")

    baseline = results.get('commutator_random_baseline', 0)
    cn = results.get('commutator_norms', {})
    if cn:
        print(f"\nCommutator norms (random baseline sqrt(2/N) = {baseline:.4f}):")
        for pair, norm in cn.items():
            ratio = norm / baseline if baseline > 0 else 0
            print(f"  {pair}: {norm:.4f} ({ratio:.2f}x baseline)")

    bs = results.get('block_structure')
    if bs:
        print(f"\nBlock structure detected:")
        print(f"  Blocks: {bs['n_blocks']}, sizes: {bs['block_sizes']}")
        print(f"  Coupling ratio (inter/intra): {bs['coupling_ratio']:.4f}")
    elif dep >= 0.99:
        print(f"\nNo block structure (no constraints detected)")

    ws = results.get('weight_stats', {})
    if ws:
        print(f"\nWeight distribution stats:")
        for name, s in ws.items():
            print(f"  {name}: std={s['std']:.4f}, kurtosis={s['kurtosis']:.2f}")

    print(f"\nElapsed: {results.get('elapsed_seconds', 0):.2f}s")
    print(f"{'='*60}\n")
